// Base HTML template generator
function render_template($template_path,$template_data)
{
	// Stores output buffer
	$html = '';
	// Start output buffering
	ob_start();
	require($template_path);
	$html = ob_get_clean();

	// Return generated (buffered) HTML
	return $html;		
}

